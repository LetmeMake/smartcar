#ifndef __DataClass_h__
#define __DataClass_h__
/*******************************************************************************
* 说    明 * :	数据类型定义
* 修改时间 * :	2016-5-14
*******************************************************************************/
typedef unsigned char        _INT8U_;		// 1字节无符号整数
typedef unsigned short int  _INT16U_;		// 2字节无符号整数
typedef unsigned long int   _INT32U_;		// 4字节无符号整数
typedef unsigned long long  _INT64U_;		// 8字节无符号整数

typedef signed char          _INT8S_;		// 1字节有符号整数
typedef signed short int    _INT16S_;		// 2字节有符号整数
typedef signed long int     _INT32S_;		// 4字节有符号整数
typedef signed long long    _INT64S_;		// 8字节有符号整数

typedef float                 _FP32_;		// 4字节浮点数
typedef double                _FP64_;		// 8字节浮点数
typedef long double           _FP80_;		// 10字节浮点数



#endif
